import Foundation
// TODO: Codable과 Equatable 추가
struct Todo: Codable, Equatable {
    let id: Int
    var isDone: Bool
    var detail: String
    var isToday: Bool
    
    // mutating 키워드를 사용하여 지속적으로 내부 값을 변경하는 것
    mutating func update(isDone: Bool, detail: String, isToday: Bool) {
        // [x] TODO: update 로직 추가
        self.isDone = isDone
        self.detail = detail
        self.isToday = isToday
    }
    
    // 동등비교를 위해 Equatable를 채택. Todo 객체들끼리의 비교를 위해 필요한 것 <- 솔직히 왜 사용하는건지는 모르겠음
    static func == (lhs: Self, rhs: Self) -> Bool {
        // [x]TODO: 동등 조건 추가
        return lhs.id == rhs.id
    }
}

// MARK: - ViewModel은 TodoManager에게 도움을 받는다
class TodoManager {
    
    // 싱글톤 객체 만들기
    static let shared = TodoManager()
    
    static var lastId: Int = 0
    
    var todos: [Todo] = []
    
    // createTodo 메서드는 TodoListViewController의 Line55에게 도움을 준다
    // 메서드의 반환 형태 + return 형식 확인하기. 다시한번 위에서 정의했던 struct 모습을 가질 수 있게 도와준다.
    func createTodo(detail: String, isToday: Bool) -> Todo {
        // [x] TODO: create로직 추가
        let nextId = TodoManager.lastId + 1
        TodoManager.lastId = nextId
        return Todo(id: nextId, isDone: false, detail: detail, isToday: isToday)
    }
    
    func addTodo(_ todo: Todo) {
        // [x] TODO: add로직 추가
        todos.append(todo)
        saveTodo()
    }
    
    // filter라는게 조건이 true이면 값 포함, false이면 값 배제인데 한번에 와닿지않는 코드...
    func deleteTodo(_ todo: Todo) {
        // [x] TODO: delete 로직 추가
        print("todos--->\(todos)")
        todos = todos.filter { $0.id != todo.id }   // todos 필터링해서 첫번째 인덱스가 $0에 들어가고 그것이 todo의 id와 같지 않은 것을 반환
        print("todo result--->\(todos)")
//        todos = todos.filter { existingTodo in
//            return existingTodo.id != todo.id
//        }
//        if let index = todos.firstIndex(of: todo) {
//            todos.remove(at: index)
//        }
        saveTodo()
    }
    
    func updateTodo(_ todo: Todo) {
        // [x] TODO: updatee 로직 추가
        guard let index = todos.firstIndex(of: todo) else { return }
        todos[index].update(isDone: todo.isDone, detail: todo.detail, isToday: todo.isToday)
        saveTodo()
    }
    
    func saveTodo() {
        Storage.store(todos, to: .documents, as: "todos.json")
    }
    
    func retrieveTodo() {
        todos = Storage.retrive("todos.json", from: .documents, as: [Todo].self) ?? []
        
        let lastId = todos.last?.id ?? 0
        TodoManager.lastId = lastId
    }
}

class TodoViewModel {
    
    enum Section: Int, CaseIterable {
        case today
        case upcoming
        
        var title: String {
            switch self {
            case .today: return "Today"
            default: return "Upcoming"
            }
        }
    }
    
    private let manager = TodoManager.shared
    
    var todos: [Todo] {
        return manager.todos
    }
    
    var todayTodos: [Todo] {
        return todos.filter { $0.isToday == true }
    }
    
    var upcompingTodos: [Todo] {
        return todos.filter { $0.isToday == false }
    }
    
    var numOfSection: Int {
        return Section.allCases.count
    }
    
    func addTodo(_ todo: Todo) {
        manager.addTodo(todo)
    }
    
    func deleteTodo(_ todo: Todo) {
        manager.deleteTodo(todo)
    }
    
    func updateTodo(_ todo: Todo) {
        manager.updateTodo(todo)
    }
    
    func loadTasks() {
        manager.retrieveTodo()
    }
}
